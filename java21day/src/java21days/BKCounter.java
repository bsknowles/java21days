// a simple counter using a for loop to count to 1 million

package java21days;

class BKCounter {
    public static void main(String[] arguments) {
        
        int i = 0;
        
        for (i = 0; i < 1000001; i++) {
            System.out.println(i);
        }
        
// BUILD SUCCESSFUL (total time: 18 seconds)  

    }
}